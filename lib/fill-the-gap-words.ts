/**
 * Get random words from the word list
 * @param count - Number of unique words to return (default: 4)
 * @returns Array of unique random words
 */
export function getRandomWords(count: number = 4): string[] {
  const shuffled = [...FILL_THE_GAP_WORDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, FILL_THE_GAP_WORDS.length));
}

